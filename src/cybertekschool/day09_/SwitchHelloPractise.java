package cybertekschool.day09_;

public class SwitchHelloPractise {
    public static void main(String[] args) {


        int languageNumber = 3;

        switch (languageNumber) {

            case 1:
                System.out.println("Hello SDET");
                break;
            case 2:
                System.out.println("Privet, SDET");
                break;
            case 3:
                System.out.println("Merhaba, SDET");
                break;
            case 4:
                System.out.println("Salam, SDET!!");
                break;
            default:
                System.out.println(" unknown language!!!");

        }


    }
}
