package cybertekschool.day07_;

public class RelationalOperator {
    public static void main(String[] args) {

        // >,<, <=, >=, ==, != relational operators
        //RO used to compare two operands
        //and return True or False
        //== used to check for equality
        //and very very from single '='
        //can be called boolean

        //!= used to check for inequality, in programming translate NOT to
        // ==equality check, but = equal mark
        // == and != are questions, cheking for equality or inequality

        int myNumber = 10;

        System.out.println(myNumber >5);
        System.out.println(myNumber<5);
        System.out.println(myNumber>=5);
        System.out.println(myNumber<=5);
        System.out.println(myNumber==5);
        System.out.println(myNumber!=5);


    }
}
