package cybertekschool.day17_;

public class MonkeySong {
    public static void main(String[] args) {

        int monkey = 5;

        while (monkey>=2){
            System.out.println(monkey + " little monkeys jumping on the bed,\n" +
                    "1 fell down and bumped his head,\n" +
                    "Mama called the doctor and the doctor said,\n" +
                    "No more monkeys jumping on the bed!");
            monkey--;
        }
        System.out.println("1 little monkey jumping on the bed,\n" +
                "One fell down and bumped his head,\n" +
                "Mama called the doctor and the doctor said,\n" +
                "No more monkeys jumping on the bed!\n" +
                "Put those monkeys right to bed!");
    }
}
